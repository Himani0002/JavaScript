// Q7. Even Sum Below N

let n = 6;
let sum = 0;

console.log("\n----------------------------------------\n");
for (let i = 0; i <= 6; i++) {
    if (i % 2 == 0) {
        sum = n + n;
    }
}
console.log("Even number sum :- " + sum);
console.log("\n----------------------------------------\n");