// Q7. Find odd or even with using function

let n = 20
function OddOrEven() {

    if (n % 2 == 0) {
        console.log("it's an even number")
    }
    else {
        console.log("it's an odd number")
    }
}

console.log("\n-------------------------------------\n")
OddOrEven()
console.log("\n-------------------------------------\n")